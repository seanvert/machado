import NavBar from "./components/navbar";
import Head from "next/head";
import { api } from "~/utils/api";
import { useForm, type SubmitHandler } from "react-hook-form"
import { Textarea } from "@/components/ui/textarea"

type Inputs = {
  textAreaID: string
}

const TextWizard = () =>  {
  const { mutate, isLoading } = api.post.create.useMutation()
  const {
    register,
    handleSubmit,
    watch,
    formState: { errors },
  } = useForm<Inputs>()

  const onSubmit: SubmitHandler<Inputs> = (data) => {
    mutate({
      name: data.textAreaID
    })
  }

  return (
    /* "handleSubmit" will validate your inputs before invoking "onSubmit" */
    <form onSubmit={handleSubmit(onSubmit)}>
      {/* register your input into the hook by invoking the "register" function */}
      <Textarea {...register("textAreaID", { required: true})} />
      {errors.textAreaID && <span>This field is required</span>}
      <div>{watch("textAreaID") ? watch("textAreaID")?.split(" ").length - 1 : 0}</div>
      <input disabled={isLoading} type="submit" />
    </form>
  )
}

export default function Home() {
  const hello = api.post.hello.useQuery({ text: "from tRPC" });

  return (
    <>
      <Head>
        <title>Machado</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className="flex min-h-screen flex-grow flex-col items-center bg-gradient-to-b from-amber-300 to-slate-600">
      <NavBar />
      <div className="flex-grow min-w-min">
        <TextWizard />
        </div>
      </main>
    </>
  );
}


